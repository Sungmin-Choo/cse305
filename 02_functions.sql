-- ============================================================
-- TRIGGER 1: Auto-generate physical seats on SEAT_CLASS INSERT
-- ============================================================
-- Business logic automation: when a SEAT_CLASS row is inserted,
-- automatically populate SEAT_INVENTORY with physical seats
-- based on seat_count.
--
-- Seat numbering rules:
--   First:    rows 1+,  columns A/B         (2 seats/row)
--   Business: rows 10+, columns A/B/C/D     (4 seats/row)
--   Economy:  rows 20+, columns A/B/C/D/E/F (6 seats/row)
-- ============================================================
CREATE OR REPLACE FUNCTION public.fn_auto_generate_seats()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    v_start_row  INT;       -- Starting row number for this class
    v_cols       TEXT[];     -- Column letter array
    v_cols_count INT;        -- Number of columns per row
    v_row        INT;        -- Current row number
    v_col_idx    INT;        -- Current column index
    v_seat_label TEXT;       -- Generated seat label (e.g., '1A')
BEGIN
    -- Determine row start and column layout based on class name
    CASE NEW.class_name
        WHEN 'First' THEN
            v_start_row := 1;
            v_cols := ARRAY['A', 'B'];
        WHEN 'Business' THEN
            v_start_row := 10;
            v_cols := ARRAY['A', 'B', 'C', 'D'];
        WHEN 'Economy' THEN
            v_start_row := 20;
            v_cols := ARRAY['A', 'B', 'C', 'D', 'E', 'F'];
    END CASE;

    v_cols_count := array_length(v_cols, 1);

    -- Generate physical seat records
    FOR i IN 1..NEW.seat_count LOOP
        v_row     := v_start_row + ((i - 1) / v_cols_count);
        v_col_idx := ((i - 1) % v_cols_count) + 1;
        v_seat_label := v_row::TEXT || v_cols[v_col_idx];

        INSERT INTO public."SEAT_INVENTORY" (class_id, aircraft_id, seat_number)
        VALUES (NEW.class_id, NEW.aircraft_id, v_seat_label);
    END LOOP;

    RAISE NOTICE 'Auto-generated % seats for class "%" on aircraft %',
        NEW.seat_count, NEW.class_name, NEW.aircraft_id;

    RETURN NEW;
END;
$$;

-- Register trigger: fires AFTER INSERT on SEAT_CLASS
DROP TRIGGER IF EXISTS trg_auto_generate_seats ON public."SEAT_CLASS";
CREATE TRIGGER trg_auto_generate_seats
    AFTER INSERT ON public."SEAT_CLASS"
    FOR EACH ROW
    EXECUTE FUNCTION public.fn_auto_generate_seats();


-- ============================================================
-- TRIGGER 2: Guard SEAT_CLASS updates (seat_count changes)
-- ============================================================
-- Integrity constraint: if seat_count is modified and there are
-- active bookings on any seat of that class, block the change
-- to protect data integrity. If no bookings exist, delete old
-- seats (new ones will be regenerated by the AFTER UPDATE trigger).
-- ============================================================
CREATE OR REPLACE FUNCTION public.fn_guard_seat_class_update()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    v_booked_count INT;
BEGIN
    -- Skip if seat_count has not changed
    IF NEW.seat_count = OLD.seat_count THEN
        RETURN NEW;
    END IF;

    -- Check if any seats of this class have active bookings
    SELECT COUNT(*) INTO v_booked_count
    FROM public."BOOKING" b
    JOIN public."SEAT_INVENTORY" si ON si.seat_id = b.seat_id
    WHERE si.class_id = OLD.class_id
      AND b.status = 'confirmed';

    IF v_booked_count > 0 THEN
        RAISE EXCEPTION
            'Cannot modify seat_count: % active booking(s) exist for class "%" on this aircraft.',
            v_booked_count, OLD.class_name;
    END IF;

    -- No active bookings — delete existing seats (AFTER trigger will regenerate)
    DELETE FROM public."SEAT_INVENTORY"
    WHERE class_id = OLD.class_id AND aircraft_id = OLD.aircraft_id;

    RETURN NEW;
END;
$$;

-- Register trigger: fires BEFORE UPDATE on SEAT_CLASS
DROP TRIGGER IF EXISTS trg_guard_seat_class_update ON public."SEAT_CLASS";
CREATE TRIGGER trg_guard_seat_class_update
    BEFORE UPDATE ON public."SEAT_CLASS"
    FOR EACH ROW
    EXECUTE FUNCTION public.fn_guard_seat_class_update();

-- Regenerate seats after a valid seat_count update
-- (BEFORE trigger already deleted old seats; this one creates new ones)
CREATE OR REPLACE FUNCTION public.fn_regenerate_seats_after_update()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    v_start_row  INT;
    v_cols       TEXT[];
    v_cols_count INT;
    v_row        INT;
    v_col_idx    INT;
    v_seat_label TEXT;
BEGIN
    -- Skip if seat_count has not changed
    IF NEW.seat_count = OLD.seat_count THEN
        RETURN NEW;
    END IF;

    -- Determine layout (same logic as INSERT trigger)
    CASE NEW.class_name
        WHEN 'First' THEN
            v_start_row := 1;  v_cols := ARRAY['A', 'B'];
        WHEN 'Business' THEN
            v_start_row := 10; v_cols := ARRAY['A', 'B', 'C', 'D'];
        WHEN 'Economy' THEN
            v_start_row := 20; v_cols := ARRAY['A', 'B', 'C', 'D', 'E', 'F'];
    END CASE;

    v_cols_count := array_length(v_cols, 1);

    FOR i IN 1..NEW.seat_count LOOP
        v_row     := v_start_row + ((i - 1) / v_cols_count);
        v_col_idx := ((i - 1) % v_cols_count) + 1;
        v_seat_label := v_row::TEXT || v_cols[v_col_idx];

        INSERT INTO public."SEAT_INVENTORY" (class_id, aircraft_id, seat_number)
        VALUES (NEW.class_id, NEW.aircraft_id, v_seat_label);
    END LOOP;

    RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_regenerate_seats_after_update ON public."SEAT_CLASS";
CREATE TRIGGER trg_regenerate_seats_after_update
    AFTER UPDATE ON public."SEAT_CLASS"
