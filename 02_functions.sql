-- ============================================================
-- 1) Generate Flights from Schedule
-- ============================================================
-- Creates individual flight records from recurring schedules
-- for a given date range. Uses the Schedule → Aircraft link.
-- ============================================================
CREATE OR REPLACE FUNCTION public.generate_flights(
    p_start_date date,
    p_end_date   date
)
RETURNS text
LANGUAGE plpgsql
AS $$
DECLARE
    v_count int := 0;
BEGIN
    IF p_start_date > p_end_date THEN
        RAISE EXCEPTION 'Invalid date range: start_date (%) > end_date (%)',
            p_start_date, p_end_date;
    END IF;

    INSERT INTO public."FLIGHT"(
        schedule_id, aircraft_id, flight_date, depart_time, arrival_time, status
    )
    SELECT
        fs.schedule_id,
        fs.aircraft_id,
        d.d_date::date,
        -- Departure datetime = date + scheduled time
        (d.d_date::timestamp + fs.depart_time)::timestamptz,
        -- Arrival datetime: if arrival_time <= depart_time, flight lands next day
        (
            CASE
                WHEN fs.arrival_time <= fs.depart_time
                THEN (d.d_date::timestamp + fs.arrival_time + interval '1 day')
                ELSE (d.d_date::timestamp + fs.arrival_time)
            END
        )::timestamptz,
        'scheduled'
    FROM public."FLIGHT_SCHEDULE" fs
    CROSS JOIN generate_series(p_start_date, p_end_date, interval '1 day') AS d(d_date)
    WHERE
        -- Only process dates within the schedule's validity range
        d.d_date::date BETWEEN fs.valid_from AND fs.valid_until
        -- Match operating days (supports Mon/Tue/... or 1/2/.../7 format)
        AND EXISTS (
            SELECT 1
            FROM unnest(string_to_array(
                replace(lower(fs.days_of_week), ' ', ''), ','
            )) AS t(tok)
            WHERE t.tok = lower(to_char(d.d_date::date, 'Dy'))
               OR t.tok = extract(isodow FROM d.d_date)::int::text
        )
        -- Skip dates that already have a generated flight
        AND NOT EXISTS (
            SELECT 1
            FROM public."FLIGHT" f
            WHERE f.schedule_id = fs.schedule_id
              AND f.flight_date = d.d_date::date
        );

    GET DIAGNOSTICS v_count = ROW_COUNT;
    RETURN v_count || ' individual flights have been generated from the recurring schedules.';
END;
$$;


-- ============================================================
-- 2) Flight Search
-- ============================================================
-- Searches available flights by departure/arrival airport,
-- travel date, and optional seat class.
-- Returns stopover_list (comma-separated airport codes) via string_agg.
-- ============================================================
CREATE OR REPLACE FUNCTION public.search_flights(
    p_dep_iata    varchar,
    p_arr_iata    varchar,
    p_travel_date date,
    p_class_name  varchar DEFAULT NULL
)
RETURNS TABLE(
    flight_id       uuid,
    flight_number   varchar,
    airline_name    varchar,
    depart_time     timestamptz,
    arrival_time    timestamptz,
    class_name      varchar,
    price           numeric,
    available_seats bigint,
    stopover_list   text
)
LANGUAGE plpgsql
AS $$
BEGIN
    RETURN QUERY
    SELECT
        v.flight_id,
        v.flight_number,
        v.airline_name,
        v.depart_time,
        v.arrival_time,
        v.class_name,
        v.price,
        v.available_seats,
        -- Build stopover list from STOPOVER table using string_agg
        (
            SELECT string_agg(s.airport_iata, ',' ORDER BY s.stop_order)
            FROM public."STOPOVER" s
            WHERE s.schedule_id = v.schedule_id
        ) AS stopover_list
    FROM public."FLIGHT_AVAILABILITY_VIEW" v
    WHERE v.depart_airport_iata = p_dep_iata
      AND v.dest_airport_iata  = p_arr_iata
      AND v.flight_date        = p_travel_date
      AND (p_class_name IS NULL OR v.class_name = p_class_name)
      AND v.available_seats > 0
    ORDER BY v.depart_time;
END;
$$;


-- ============================================================
-- 3) Create Booking (atomic transaction)
-- ============================================================
-- Atomically performs: seat reservation → payment → ticketing.
-- The trigger trg_validate_booking auto-validates seat/flight
-- integrity before the INSERT completes.
-- ============================================================
CREATE OR REPLACE FUNCTION public.create_booking(
    p_customer_id uuid,
    p_flight_id   uuid,
    p_seat_id     uuid,
    p_amount      numeric
)
RETURNS uuid
LANGUAGE plpgsql
AS $$
DECLARE
    v_booking_id uuid;
BEGIN
    -- Validate payment amount
    IF p_amount <= 0 THEN
        RAISE EXCEPTION 'Payment amount must be > 0';
    END IF;

    -- Step 1: Create booking (trigger validates seat/flight integrity)
    BEGIN
        INSERT INTO public."BOOKING"(flight_id, customer_id, seat_id, status, price)
        VALUES (p_flight_id, p_customer_id, p_seat_id, 'confirmed', p_amount)
        RETURNING booking_id INTO v_booking_id;
    EXCEPTION WHEN unique_violation THEN
        RAISE EXCEPTION 'The selected seat is already occupied for this flight.';
    END;

    -- Step 2: Process payment
    INSERT INTO public."PAYMENT"(booking_id, amount, method, status)
    VALUES (v_booking_id, p_amount, 'credit_card', 'completed');

    -- Step 3: Issue ticket (ticket_no computed from booking_id in views/queries)
    INSERT INTO public."TICKET"(booking_id)
    VALUES (v_booking_id);

    RETURN v_booking_id;
END;
$$;


-- ============================================================
-- 4) Cancel Booking and Refund (atomic transaction)
-- ============================================================
-- Atomically performs: cancel booking → refund payment → delete ticket.
-- ============================================================
CREATE OR REPLACE FUNCTION public.cancel_booking(p_booking_id uuid)
RETURNS text
LANGUAGE plpgsql
AS $$
DECLARE
    v_payment_id uuid;
    v_amount     numeric;
BEGIN
    -- Verify the booking exists and is active
    IF NOT EXISTS (
        SELECT 1 FROM public."BOOKING"
        WHERE booking_id = p_booking_id AND status = 'confirmed'
    ) THEN
        RAISE EXCEPTION 'Booking not found or already cancelled.';
    END IF;

    -- Lock and retrieve the associated payment (FOR UPDATE for concurrency)
    SELECT p.payment_id, p.amount
        INTO v_payment_id, v_amount
    FROM public."PAYMENT" p
    WHERE p.booking_id = p_booking_id
        AND p.status = 'completed'
    FOR UPDATE;

    IF v_payment_id IS NULL THEN
        RAISE EXCEPTION 'No completed payment found for booking %', p_booking_id;
    END IF;

    -- Delete ticket first (FK references BOOKING)
    DELETE FROM public."TICKET"
    WHERE booking_id = p_booking_id;

    -- Mark booking as cancelled
    UPDATE public."BOOKING"
    SET status = 'cancelled'
    WHERE booking_id = p_booking_id;

    -- Mark payment as refunded
    UPDATE public."PAYMENT"
    SET status = 'refunded'
    WHERE payment_id = v_payment_id;

    -- Create refund record
    INSERT INTO public."REFUND"(payment_id, amount, status, refunded_at)
    VALUES (v_payment_id, v_amount, 'completed', now());

    RETURN 'Booking ' || p_booking_id || ' has been successfully cancelled and refunded.';
END;
$$;


-- ============================================================
-- 5) Revenue Statistics Report
-- ============================================================
-- Aggregates revenue by flight, seat class, route, and time period.
-- Includes load factor (seat occupancy rate) per class.
-- ============================================================
CREATE OR REPLACE FUNCTION public.get_revenue_report()
RETURNS TABLE (
    flight_id               uuid,
    flight_number           varchar,
    airline_name            varchar,
    route                   text,
    flight_date             date,
    revenue_month           timestamptz,
    revenue_quarter         timestamptz,
    class_name              varchar,
    total_revenue           numeric,
    class_revenue_pct       numeric,
    load_factor_percentage  numeric
)
LANGUAGE plpgsql
AS $$
BEGIN
    RETURN QUERY
    SELECT
        v.flight_id,
        v.flight_number,
        v.airline_name,
        v.depart_airport_iata || ' -> ' || v.dest_airport_iata AS route,
        v.flight_date,
        v.revenue_month,
        v.revenue_quarter,
        v.class_name,
        v.revenue AS total_revenue,
        ROUND(
            100 * v.revenue
            / NULLIF(SUM(v.revenue) OVER (PARTITION BY v.flight_id, v.flight_date), 0),
            2
        ) AS class_revenue_pct,
        v.load_factor_pct AS load_factor_percentage
    FROM public."REVENUE_STATS_VIEW" v
    ORDER BY v.flight_date DESC, v.revenue DESC;
END;
$$;
