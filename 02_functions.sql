    EXECUTE FUNCTION public.fn_regenerate_seats_after_update();


-- ============================================================
-- TRIGGER 3: Validate booking integrity (BEFORE INSERT on BOOKING)
-- ============================================================
-- Integrity constraint enforced via trigger:
--   1) The seat must belong to the same aircraft as the flight
--   2) The flight must be in 'scheduled' status
-- ============================================================
CREATE OR REPLACE FUNCTION public.fn_validate_booking()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    v_flight_aircraft_id uuid;
    v_seat_aircraft_id   uuid;
    v_flight_status      varchar;
BEGIN
    -- Look up the flight's aircraft and status
    SELECT f.aircraft_id, f.status
      INTO v_flight_aircraft_id, v_flight_status
    FROM public."FLIGHT" f
    WHERE f.flight_id = NEW.flight_id;

    IF v_flight_aircraft_id IS NULL THEN
        RAISE EXCEPTION 'Flight % does not exist.', NEW.flight_id;
    END IF;

    -- Ensure the flight is bookable
    IF v_flight_status != 'scheduled' THEN
        RAISE EXCEPTION 'Cannot book: flight status is "%" (must be "scheduled").', v_flight_status;
    END IF;

    -- Look up the seat's aircraft
    SELECT si.aircraft_id
      INTO v_seat_aircraft_id
    FROM public."SEAT_INVENTORY" si
    WHERE si.seat_id = NEW.seat_id;

    IF v_seat_aircraft_id IS NULL THEN
        RAISE EXCEPTION 'Seat % does not exist.', NEW.seat_id;
    END IF;

    -- Ensure the seat belongs to the flight's aircraft
    IF v_seat_aircraft_id != v_flight_aircraft_id THEN
        RAISE EXCEPTION 'Seat does not belong to the aircraft assigned to this flight.';
    END IF;

    RETURN NEW;
END;
$$;

-- Register trigger: fires BEFORE INSERT on BOOKING
DROP TRIGGER IF EXISTS trg_validate_booking ON public."BOOKING";
CREATE TRIGGER trg_validate_booking
    BEFORE INSERT ON public."BOOKING"
    FOR EACH ROW
    EXECUTE FUNCTION public.fn_validate_booking();


